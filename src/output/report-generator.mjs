/**
 * Report Generator
 *
 * Reads shared-findings.jsonl from a completed job's state directory and
 * writes human-readable output files to the job's output directory.
 *
 * Generates:
 *   report.md     — Markdown report grouped by category and clinical significance
 *   findings.json — Structured JSON of all findings (full detail)
 *   summary.txt   — Plain-text executive summary
 */

import { existsSync, readFileSync, writeFileSync, mkdirSync } from "fs";
import { join } from "path";

// ---------------------------------------------------------------------------
// Entry point
// ---------------------------------------------------------------------------

/**
 * Generate reports from a completed job's state directory.
 *
 * @param {string} stateDir  - Path to the job state directory
 * @param {string} outputDir - Path to write report files
 * @param {object} config    - Pipeline config (for metadata)
 * @returns {string[]}       - List of generated file paths
 */
export async function generateReports(stateDir, outputDir, config) {
  mkdirSync(outputDir, { recursive: true });

  const findings   = readJsonl(join(stateDir, "shared-findings.jsonl"));
  const chat       = readJsonl(join(stateDir, "agent-chat.jsonl"));
  const jobMeta    = readJson(join(stateDir, "job-meta.json")) || {};

  const generated = [];

  // 1. findings.json — full structured data
  const findingsPath = join(outputDir, "findings.json");
  writeFileSync(findingsPath, JSON.stringify({ meta: jobMeta, findings, chat }, null, 2));
  generated.push(findingsPath);

  // 2. report.md — readable Markdown report
  const mdPath = join(outputDir, "report.md");
  writeFileSync(mdPath, buildMarkdownReport(findings, jobMeta, config));
  generated.push(mdPath);

  // 3. summary.txt — plain-text executive summary
  const txtPath = join(outputDir, "summary.txt");
  writeFileSync(txtPath, buildTextSummary(findings, jobMeta, config));
  generated.push(txtPath);

  return generated;
}

// ---------------------------------------------------------------------------
// Markdown report builder
// ---------------------------------------------------------------------------

function buildMarkdownReport(findings, jobMeta, config) {
  const lines = [];
  const now = new Date().toISOString().split("T")[0];

  lines.push("# Genomic Health Report");
  lines.push("");
  lines.push(`**Generated:** ${now}  `);
  if (jobMeta.jobId) lines.push(`**Job ID:** ${jobMeta.jobId}  `);
  if (jobMeta.config?.preset) lines.push(`**Preset:** ${jobMeta.config.preset}  `);
  lines.push("");
  lines.push("> **Medical Disclaimer:** This report is generated by AI agents querying public");
  lines.push("> genomics databases and is intended for informational purposes only. It is not");
  lines.push("> a substitute for professional medical advice, diagnosis, or treatment. Please");
  lines.push("> consult a qualified healthcare provider or genetic counsellor before making");
  lines.push("> any health decisions based on this information.");
  lines.push("");

  if (!findings.length) {
    lines.push("No findings were recorded in this run.");
    return lines.join("\n");
  }

  // Group findings by category
  const bySeverity = groupBy(findings, f => normaliseSeverity(f.significance || f.clinical_significance || f.category));
  const severityOrder = ["high", "moderate", "low", "informational", "drug_response", "protective", "other"];

  const severityLabels = {
    high:          "High Clinical Significance",
    moderate:      "Moderate Clinical Significance",
    low:           "Low / Uncertain Significance",
    informational: "Informational",
    drug_response: "Pharmacogenomics / Drug Response",
    protective:    "Protective Variants",
    other:         "Other Findings",
  };

  lines.push(`## Summary`);
  lines.push("");
  lines.push(`Total findings: **${findings.length}**`);
  lines.push("");

  // Mini table of contents by severity
  for (const severity of severityOrder) {
    const group = bySeverity[severity];
    if (group?.length) {
      lines.push(`- ${severityLabels[severity]}: ${group.length} finding${group.length > 1 ? "s" : ""}`);
    }
  }
  lines.push("");

  // Detailed sections per severity
  for (const severity of severityOrder) {
    const group = bySeverity[severity];
    if (!group?.length) continue;

    lines.push(`---`);
    lines.push(`## ${severityLabels[severity]}`);
    lines.push("");

    // Sort within group: pathogenic first, then by gene name
    const sorted = group.sort((a, b) => {
      const aGene = a.gene || a.genes?.[0] || "";
      const bGene = b.gene || b.genes?.[0] || "";
      return aGene.localeCompare(bGene);
    });

    for (const finding of sorted) {
      const gene  = finding.gene || finding.genes?.join(", ") || "Unknown gene";
      const rsid  = finding.rsid || finding.variant_id || "";
      const title = finding.title || finding.summary || `${gene} variant`;

      lines.push(`### ${gene}${rsid ? ` (${rsid})` : ""}`);
      lines.push("");
      lines.push(`**${title}**`);
      lines.push("");

      if (finding.description || finding.details) {
        lines.push(finding.description || finding.details);
        lines.push("");
      }

      // Key facts as a table if available
      const facts = [];
      if (finding.genotype)            facts.push(["Genotype",              finding.genotype]);
      if (finding.clinical_significance) facts.push(["Clinical Significance", finding.clinical_significance]);
      if (finding.condition)           facts.push(["Condition",             finding.condition]);
      if (finding.inheritance)         facts.push(["Inheritance",           finding.inheritance]);
      if (finding.allele_frequency)    facts.push(["Population Frequency",  finding.allele_frequency]);
      if (finding.drug)                facts.push(["Drug",                  finding.drug]);
      if (finding.recommendation)      facts.push(["Recommendation",       finding.recommendation]);
      if (finding.sources?.length)     facts.push(["Sources",              finding.sources.join(", ")]);

      if (facts.length) {
        lines.push("| Field | Value |");
        lines.push("|-------|-------|");
        for (const [field, value] of facts) {
          lines.push(`| ${field} | ${String(value).replace(/\|/g, "\\|")} |`);
        }
        lines.push("");
      }

      if (finding.agent) {
        lines.push(`*Discovered by: ${finding.agent}*`);
        lines.push("");
      }
    }
  }

  lines.push("---");
  lines.push("*Report generated by [Genomic Agent Discovery](https://github.com/HelixGenomics/Genomic-Agent-Discovery)*");

  return lines.join("\n");
}

// ---------------------------------------------------------------------------
// Plain text summary builder
// ---------------------------------------------------------------------------

function buildTextSummary(findings, jobMeta, config) {
  const lines = [];
  const now = new Date().toISOString().split("T")[0];

  lines.push("GENOMIC HEALTH REPORT — EXECUTIVE SUMMARY");
  lines.push("==========================================");
  lines.push(`Generated: ${now}`);
  if (jobMeta.config?.preset) lines.push(`Preset: ${jobMeta.config.preset}`);
  lines.push("");

  lines.push("MEDICAL DISCLAIMER");
  lines.push("This report is for informational purposes only and is not a substitute");
  lines.push("for professional medical advice. Consult a healthcare provider or");
  lines.push("genetic counsellor before making health decisions.");
  lines.push("");

  if (!findings.length) {
    lines.push("No findings recorded.");
    return lines.join("\n");
  }

  lines.push(`TOTAL FINDINGS: ${findings.length}`);
  lines.push("");

  const high = findings.filter(f => normaliseSeverity(f.significance || f.clinical_significance) === "high");
  const pharma = findings.filter(f => normaliseSeverity(f.significance || f.clinical_significance || f.category) === "drug_response");

  if (high.length) {
    lines.push("HIGH SIGNIFICANCE FINDINGS");
    lines.push("--------------------------");
    for (const f of high) {
      const gene = f.gene || f.genes?.join(", ") || "Unknown";
      lines.push(`• ${gene}${f.rsid ? ` (${f.rsid})` : ""}: ${f.title || f.summary || f.condition || "Variant detected"}`);
    }
    lines.push("");
  }

  if (pharma.length) {
    lines.push("PHARMACOGENOMICS FINDINGS");
    lines.push("-------------------------");
    for (const f of pharma) {
      const gene = f.gene || f.genes?.join(", ") || "Unknown";
      lines.push(`• ${gene}: ${f.title || f.summary || f.drug || "Drug response variant"}`);
    }
    lines.push("");
  }

  lines.push("See report.md for the full annotated report.");
  return lines.join("\n");
}

// ---------------------------------------------------------------------------
// Helpers
// ---------------------------------------------------------------------------

function readJsonl(filePath) {
  if (!existsSync(filePath)) return [];
  try {
    return readFileSync(filePath, "utf8")
      .trim().split("\n").filter(Boolean)
      .map(line => { try { return JSON.parse(line); } catch { return null; } })
      .filter(Boolean);
  } catch {
    return [];
  }
}

function readJson(filePath) {
  if (!existsSync(filePath)) return null;
  try { return JSON.parse(readFileSync(filePath, "utf8")); } catch { return null; }
}

function groupBy(arr, keyFn) {
  const result = {};
  for (const item of arr) {
    const key = keyFn(item);
    (result[key] ??= []).push(item);
  }
  return result;
}

function normaliseSeverity(raw) {
  if (!raw) return "other";
  const s = String(raw).toLowerCase();
  if (s.includes("pathogenic") || s.includes("high"))        return "high";
  if (s.includes("likely path"))                             return "high";
  if (s.includes("moderate") || s.includes("uncertain"))     return "moderate";
  if (s.includes("drug") || s.includes("pharma") || s.includes("response")) return "drug_response";
  if (s.includes("protective") || s.includes("benign"))      return "protective";
  if (s.includes("low") || s.includes("vus"))                return "low";
  if (s.includes("info") || s.includes("trait"))             return "informational";
  return "other";
}
